let str1 = 'Welcome to Thailand';
let str2 = 'Don\'t be afraid\n';  // ใช้ backslash (\) เพื่อ escape เครื่องหมาย '
let str3 = 'สวัสดีประเทศไทย';

// หาความยาวของแต่ละตัวแปร
console.log(str1.length);  // ผลลัพธ์: 19
console.log(str2.length);  // ผลลัพธ์: 17 (มีการนับ newline ด้วย)
console.log(str3.length);  // ผลลัพธ์: 15
