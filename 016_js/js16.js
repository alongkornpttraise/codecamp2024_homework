console.log('' + 1 + 0);       // ผลลัพธ์: '10'
console.log('' - 1 + 0);       // ผลลัพธ์: -1
console.log(true + false);     // ผลลัพธ์: 1
console.log(6 / '3');          // ผลลัพธ์: 2
console.log('2' * '3');        // ผลลัพธ์: 6
console.log('   -9   ' - 5);   // ผลลัพธ์: -14
console.log('   -9   ' + 5);   // ผลลัพธ์: '   -9   5'
console.log('4px' - 2);        // ผลลัพธ์: NaN
console.log(2 + 4 + 'px');     // ผลลัพธ์: '6px'
console.log(null + 1);         // ผลลัพธ์: 1
console.log(undefined + 1);    // ผลลัพธ์: NaN
