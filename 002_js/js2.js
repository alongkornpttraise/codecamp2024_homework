// ประกาศตัวแปรที่ใช้เก็บจำนวนเงินในกระเป๋าตังของคุณ
let walletAmount = 1000; // ใส่จำนวนเงินของคุณ

// ประกาศตัวแปรที่ใช้เก็บชื่อของพ่อและแม่ของคุณ
let fatherName = "ชื่อพ่อของคุณ"; // แก้ไขเป็นชื่อพ่อของคุณจริงๆ
let motherName = "ชื่อแม่ของคุณ"; // แก้ไขเป็นชื่อแม่ของคุณจริงๆ

// ประกาศตัวแปรที่ใช้เก็บที่อยู่ของบ้านคุณ
let homeAddress = "ที่อยู่บ้านของคุณ"; // แก้ไขเป็นที่อยู่บ้านของคุณจริงๆ

// ประกาศตัวแปรที่ใช้เก็บอายุของจักรวาล
let ageOfUniverse = 13.8; // อายุของจักรวาลประมาณ 13.8 พันล้านปี

// แสดงผลตัวแปร
console.log("จำนวนเงินในกระเป๋า:", walletAmount);
console.log("ชื่อพ่อ:", fatherName);
console.log("ชื่อแม่:", motherName);
console.log("ที่อยู่บ้าน:", homeAddress);
console.log("อายุของจักรวาล:", ageOfUniverse, "พันล้านปี");
