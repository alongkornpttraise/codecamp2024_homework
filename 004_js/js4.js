// ประกาศตัวแปรเป็น const ชื่อ firstName มีค่าเป็นชื่อจริงตัวเอง
const firstName = "ชื่อจริงของคุณ"; // แก้ไขเป็นชื่อจริงของคุณ

// ประกาศตัวแปรเป็น let ชื่อ lastName มีค่าเป็นนามสกุลตัวเอง
let lastName = "นามสกุลของคุณ"; // แก้ไขเป็นนามสกุลของคุณ

// ประกาศตัวแปรเป็น var ชื่อ nickname มีค่าเป็นชื่อเล่นตัวเอง
var nickname = "ชื่อเล่นของคุณ"; // แก้ไขเป็นชื่อเล่นของคุณ

// ประกาศตัวแปรทั้ง 3 ซ้ำอีกรอบโดยใช้ชื่อตัวแปรเดิม และให้ใส่ข้อมูลของเพื่อนลงในตัวแปร
const firstNameFriend = "ชื่อจริงของเพื่อน"; // แก้ไขเป็นชื่อจริงของเพื่อน
let lastNameFriend = "นามสกุลของเพื่อน"; // แก้ไขเป็นนามสกุลของเพื่อน
var nicknameFriend = "ชื่อเล่นของเพื่อน"; // แก้ไขเป็นชื่อเล่นของเพื่อน

// ประกาศตัวแปรชื่อ birthDate โดยไม่ต้องใส่ const let หรือ var นำหน้าชื่อตัวแปร และกำหนดค่าเริ่มต้นเป็นวันเกิดของตัวเอง
birthDate = "วันเกิดของคุณ"; // แก้ไขเป็นวันเกิดของคุณ (เช่น "01/01/1990")

// แสดงผลตัวแปรทั้งหมด
console.log("ชื่อจริงของคุณ:", firstName);
console.log("นามสกุลของคุณ:", lastName);
console.log("ชื่อเล่นของคุณ:", nickname);
console.log("ชื่อจริงของเพื่อน:", firstNameFriend);
console.log("นามสกุลของเพื่อน:", lastNameFriend);
console.log("ชื่อเล่นของเพื่อน:", nicknameFriend);
console.log("วันเกิดของคุณ:", birthDate);
