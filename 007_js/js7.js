const country = 'Thailand';
const continent = 'Asia';

console.log(`number is ${2}`); // ผลลัพธ์: number is 2
console.log(`result is ${1 + 3}`); // ผลลัพธ์: result is 4
console.log(`I live in ${country}`); // ผลลัพธ์: I live in Thailand
console.log(`I live in ${country}, ${continent}`); // ผลลัพธ์: I live in Thailand, Asia
console.log(`I live in ${country + ', ' + continent}`); // ผลลัพธ์: I live in Thailand, Asia
console.log(`I live in ${'country, continent'}`); // ผลลัพธ์: I live in country, continent
